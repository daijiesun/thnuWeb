<template>
    <!-- 寻物启事 -->
    <div class="thnu_lose">
        <h4 class="text-primary">服务管理 > 寻物启事 </h4>
        <div class="table-responsive">
            <table class="table table-hover">
                <thead class="text-primary">
                    <tr>
                        <td>序号</td>
                        <td>发布人</td>
                        <td>联系方式</td>
                        <td>发布时间</td>
                        <td>内容</td>
                        <td>移除</td>
                    </tr>
                </thead>
                <tbody>
                    <tr v-for="(item,index) in loseList" :key="item._id">
                        <td>{{index + 1}}</td>
                        <td>{{item.userName}}</td>
                        <td>{{item.tel}}</td>
                        <td>{{item.date}}</td>
                        <td>{{item.content}}</td>
                        <!-- <td><img :src="item.photo[0]" alt=""></td> -->
                        <td>
                            <span class="pointer glyphicon glyphicon-trash" @click="removeOne(index,item._id)"></span>
                        </td>
                    </tr>
                </tbody>
            </table>
        </div>
    </div>
</template>
<script>
export default {
    data() {
        return {
            loseList: []
        };
    },
    mounted() {
        this.getList();
    },
    methods: {
        getList: function() {
            this.axios
                .get("admin/getLoseList")
                .then(res => {
                    if (res.data.status == "success") {
                        this.loseList = res.data.message.reverse();
                    } else {
                        this.$toast.center(res.data.message);
                    }
                })
                .catch(err => {
                    console.log(err);
                });
        },
        removeOne(index, id) {
            const result = confirm("确定移除？一旦移除不可恢复");
            if (result) {
                this.axios
                    .get("admin/delOneLose" + "?id=" + id)
                    .then(res => {
                        if (res.data.status == "success") {
                            this.$toast.center(res.data.message);
                            this.loseList.splice(index, 1);
                        } else {
                            this.$toast.center(res.data.message);
                        }
                    })
                    .catch(err => {
                        console.log(err);
                        this.$toast.center("系统繁忙，请稍后再试");
                    });
            }
            return;
        }
    }
};
</script>

